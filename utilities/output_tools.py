def add_to_txt_new_line(TxtPath , Str2add):
    # Adds string [Str2add] to new line in file txt [TxtPath].
    # TxtPath - string path to txt file including filename.txt
    # Str2add - string which we want to add.

    # QA
    if(TxtPath[-4:] != '.txt'):
        raise NameError('Invalid TxtPath')

        return 0

    else:

        with open( TxtPath , 'a') as txt_file:  # https://www.geeksforgeeks.org/with-statement-in-python/
             txt_file.write( "\n" + Str2add + "\n" )

        return 1

